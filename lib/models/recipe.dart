class Recipe {
  final String name;
  final String image;
  final String description;
  final String time;
  final String rating;
  final String ingredients;
  final String steps;

  Recipe({
    required this.name,
    required this.image,
    required this.description,
    required this.time,
    required this.rating,
    required this.ingredients,
    required this.steps,
  });
}
